package net.patowen.songanalyzer.grid;

public class GridColumn {
	private int xPos;
	private int width;
	
	public void setXPos(int xPos) {
		this.xPos = xPos;
	}
	
	public void setWidth(int width) {
		this.width = width;
	}
	
	public int getXPos() {
		return xPos;
	}
	
	public int getWidth() {
		return width;
	}
}
