package net.patowen.songanalyzer.grid;

public class GridRow {
	private int yPos;
	private int height;
	
	public void setXPos(int yPos) {
		this.yPos = yPos;
	}
	
	public void setWidth(int height) {
		this.height = height;
	}
	
	public int getYPos() {
		return yPos;
	}
	
	public int getHeight() {
		return height;
	}
}
